#==,!=,>=,<=,<,>
print("Operador (==)")
print(5==7) #Respuesta false
print(5==5) #Respuesta true
print(5.0==5)#Respuesta true
print("\n")

print("Operador DIFERENTE O NO IGUAL(!=)")
print(5 !=5) #False
print(10!=7) #True
print(5.0 !=5) #False

print("Operador MAYOR QUE (>)")
print(10>5) #True
print("\n") #False

print("Operador MENOR QUE (<)")
print(5<10) #True
print(10<5) #False
print("\n")

print("Operador menor o igual que (<=)")
print(5<=5) #True
print(10<=7) #False
print(3<=5) #True

print("\n Operador MAYOR O IGUAL QUE")
print(5>=5) #True
print(10>=7) #False
print(3>=5) #True

print("\u263A")